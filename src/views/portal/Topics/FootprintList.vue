<script setup lang="ts">
import { ref, onMounted } from 'vue'
import { useRouter } from 'vue-router'
import { getFootprintPageList } from '@/api/redCulture'
import type { FootprintItem } from '@/api/redCulture'

const router = useRouter()

// 状态
const loading = ref(false)
const footprintList = ref<FootprintItem[]>([])
const searchKeyword = ref('')
const totalCount = ref(0)
const currentPage = ref(1)
const pageSize = ref(12)
const hasMore = ref(true)

// 格式化日期
const formatDate = (dateStr: string) => {
  if (!dateStr) return ''
  const date = new Date(dateStr)
  const year = date.getFullYear()
  const month = String(date.getMonth() + 1).padStart(2, '0')
  const day = String(date.getDate()).padStart(2, '0')
  return `${year}-${month}-${day}`
}

// 获取足迹列表
const fetchFootprintList = async (append = false) => {
  loading.value = true
  try {
    const params: any = {
      pageIndex: currentPage.value,
      pageSize: pageSize.value
    }
    
    // 添加搜索关键词
    if (searchKeyword.value) {
      params.keyword = searchKeyword.value
    }
    
    const response = await getFootprintPageList(params)
    
    if (response && response.records) {
      if (append) {
        footprintList.value = [...footprintList.value, ...response.records]
      } else {
        footprintList.value = response.records
      }
      totalCount.value = response.total
      hasMore.value = currentPage.value < response.pages
    }
  } catch (error) {
    console.error('获取足迹列表失败：', error)
  } finally {
    loading.value = false
  }
}

// 搜索
const handleSearch = () => {
  currentPage.value = 1
  fetchFootprintList(false)
}

// 加载更多
const loadMore = () => {
  if (!hasMore.value || loading.value) return
  currentPage.value++
  fetchFootprintList(true)
}

// 点击列表项
const handleItemClick = (item: FootprintItem) => {
  // 根据 footprintType 决定跳转方式
  if (item.footprintType === 0) {
    // 富文本内容，跳转到详情页
    router.push(`/topics/footprint/${item.id}`)
  } else if (item.footprintType === 1) {
    // URL地址，外部跳转
    window.open(item.content, '_blank')
  }
}

onMounted(() => {
  fetchFootprintList()
})
</script>

